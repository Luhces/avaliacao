<script>
    $('[data-accordion]').on('click', function(e){
    e.preventDefault();
    if(!$(this).hasClass('active')){ 
        $('[data-accordion].active').removeClass('active');
        $('[data-accordion]').siblings('.content-accordion').slideUp();
        $(this).addClass('active');
        $(this).siblings('.content-accordion').slideDown();
    } else {
        $(this).removeClass('active');
        $(this).siblings('.content-accordion').slideUp();

    }
    
    });
</script>